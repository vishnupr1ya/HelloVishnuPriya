package javamethod;

public class Encapsulation {
	public int getActno() {
		return actno;
	}
	public void setActno(int actno) {
		this.actno = actno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getActbal() {
		return actbal;
	}
	public void setActbal(double actbal) {
		this.actbal = actbal;
	}
	private int actno;
	private String name;
	private double actbal;

}
